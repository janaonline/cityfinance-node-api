function getInputKeysByType(formType, type, label, dataSource = null, position, required = true, mn = false, info = "") {
    let maximum = 9999999999
    let min = 0
    if (formType != "number") {
        min = 0
        maximum = 50
    }
    if (mn == true) {
        min = 6000000000
        maximum = 9999999999
    }
    let obj = {
        label: label,
        max: maximum,
        min: min,
        placeHolder: "",
        postion: position,
        modelName: dataSource, // USER | LEDGER
        formFieldType: formType, // text | number | radio-toggle | file
        required: required,
        type: type,
        canShow: true,
    }
    if (info !== "") {
        obj.info = info
    }
    return obj
}

