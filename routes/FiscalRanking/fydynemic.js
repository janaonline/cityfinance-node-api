function getInputKeysByType(formType, type, label, dataSource = null, position, required = true) {
    let maximum = 9999999999
    let min = 0
    if (formType != "number") {
        min = 0
        maximum = 50
    }
    if (label == "Mobile number") {
        min = 6000000000
        maximum = 9999999999
    }
    return {
        label: label,
        max: maximum,
        min: min,
        placeHolder: "",
        postion: position,
        modelName: dataSource, // USER | LEDGER
        formFieldType: formType, // text | number | radio-toggle | file
        required: required,
        type: type,
        canShow: true
    }
}

