function jsonObject(arr) {
    try {
        let returnable = {}
        for (let obj of arr) {
            returnable[obj.key] = {
                key: obj.key,
                label: obj.label,
                displayPriority: obj.displayPriority,
                yearData: []
            }
            for (let index in obj.years) {
                let year = obj.years[index]
                let temp = {
                    label: `FY ${year}`,
                    key: `FY${year}`,
                    postion: index,
                    value: obj.cb ? obj.cb : '',
                    min: obj.min ? obj.min : 0,
                    max: obj.max ? obj.max : 999999,
                    required: obj.isReq,
                    year: years[year],
                    readonly: obj.readOnly,
                    formFieldType: obj.fieldType,
                    type: obj.key,
                    bottomText: ``,
                    placeHolder: '',
                    input: "number"
                }

                returnable[obj.key]['yearData'].push(temp)
            }
        }
        return returnable
    }
    catch (err) {
        console.log("error in jsonObject :: ", err.message)
    }
}



function createJson(objects) {
    let json = []
    for (let key in objects) {
        let temp = {}
        temp['key'] = key
        let obj = objects[key]
        temp['label'] = obj['label']
        temp['codes'] = new Set()
        temp['years'] = []
        temp['formFieldType'] = 'number'
        temp['required'] = true
        temp['displayPriority'] = obj['displayPriority']
        temp['input'] = "number"
        for (var k of obj.yearData) {
            if (k.label) {
                let year = k.label.split(" ")[1]
                temp['years'].push(year)
            }
            if (k.code) {
                for (code of k.code) {
                    // console.log(code)
                    temp['codes'].add(code)
                }
            }

        }
        temp['codes'] = Array.from(temp['codes'])
        json.push(temp)
    }
    return json
}
